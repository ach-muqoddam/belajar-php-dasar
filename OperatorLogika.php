<?php

$a = 10;
$b = 10;

// Operator AND akan TRUE jika keduanya bernilai TRUE
var_dump($a && $b);

// Operator OR akan TRUE jika salah satu atau keduanya TRUE
var_dump($a || $b);

// Operator NOT akan TRUE jika variable yang dimaksud FALSE
var_dump(!false);
// Operator XOR akan TRUE jika perbandingan variable salah satu TRUE atau tidak keduanya
